function addListeners() {

  //Status Vars
  let max = false;
  let min = false;
  let nav = 'home';
  let windowH = window.innerHeight;

  //Element Vars
  //Header Console
  let fauxConsole = document.getElementById('fauxConsole');
  let consoleClose = document.getElementById('consoleClose');
  let consoleMax = document.getElementById('consoleMax');
  let consoleMin = document.getElementById('consoleMin');
  let consoleForm = document.getElementById("consoleForm");
  let consoleSubmit = document.getElementById('consoleSubmit');
  let closeDiv = document.getElementById('closeDiv');
  let consoleReopen = document.getElementById('consoleReopen');

  //Navigation
  let navHome = document.getElementById('home');
  let navProj = document.getElementById('proj');

  //Contact Info Overlay
  let overlay = document.getElementById('overlay');
  let overlayArea = document.getElementById('overlayArea');
  let overlayOpen = document.getElementById('overlayOpen');
  let overlayClose = document.getElementById('overlayClose');

  //Console Window Button Listeners
  consoleClose.addEventListener('click', function() {
      fauxConsole.style = `width: 0em;`;
      closeDiv.style = `width: 100%;`;
      transition = true;
    });

  consoleReopen.addEventListener('click', function() {
    fauxConsole.style = 'width: 98vw;';
    closeDiv.style = `width: 0%;`;
  });

  consoleMax.addEventListener('click', function() {
      if (!max) {
        fauxConsole.style = `width: 98vw;
                            max-width: 100vw;
                            height: 95%;
                            visibility: visible;`;
        min = false;
        max = true;
      }
      else {
        fauxConsole.style = `width: 95vw;
                            max-width: 100em;
                            height: auto;
                            visibility: visible;`;
        min = false;
        max = false;
      }
  });

  consoleMin.addEventListener('click', function() {
      if (!min) {
        fauxConsole.style = `width: 95vw;
                            max-width: 100em;
                            height: auto;
                            visibility: hidden;`;
        min = true;
        max = false;
      }
      else {
        fauxConsole.style = `width: 95vw;
                            max-width: 100em;
                            height: auto;
                            visibility: visible;`;
        min = false;
        max = false;
      }
  });

  //Console Form Functionality
  consoleSubmit.addEventListener('click', function () {
    executeInput();
  });

  consoleForm.addEventListener('submit', function (event) {
    event.preventDefault();
  });

  //Navigation Button Listeners
  loadContentArea(nav);

  navHome.addEventListener('click', function() {
    nav = 'home';
    navProj.removeClass('selected');
    navHome.addClass('selected');
    navProj.addClass('deselected');
    navHome.removeClass('deselected');
    loadContentArea(nav);
  });

  navProj.addEventListener('click', function() {
    nav = 'proj';
    navProj.addClass('selected');
    navHome.removeClass('selected');
    navProj.removeClass('deselected');
    navHome.addClass('deselected');
    loadContentArea(nav);
  });

  //Overlay Listeners
  overlayOpen.addEventListener('click', function() {
    overlay.style.width = "100%";
  });

  overlayClose.addEventListener('click', function() {
    overlay.style.width = "0%";
  });

  //Animation Listeners
  fauxConsole.addEventListener('click', function () {
    transition = true;
  });

  window.addEventListener('scroll', function() {
    animatePsuedos(windowH);
  });

  window.addEventListener('resize', function() {
    windowH = window.innerHeight;
  });
}

