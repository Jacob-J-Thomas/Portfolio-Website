function removePseudoAnimate() {
  let elements = document.getElementsByClassName('content-sect');

  for (let i = 0; i < elements.length; i++) {
    elements[i].classList.remove('animatePseudos');
  }
}

function animatePsuedos(windowH) {
  let sections = document.getElementsByClassName('content-sect');
  let footer = document.getElementById('contentFooter');
  let elementH;

  for (i = 0; i < sections.length; i++) {
    elementH = sections[i].getBoundingClientRect().top;
    let id = 'contentSect' + i;
    let currentElement = document.getElementById(id);
    if (elementH - windowH <= 0 && i < sections.length - 1) {
      currentElement.classList.add('animatePseudos');
    }
    else {
      if (elementH - windowH <= 0) {
        footer.classList.add('animatePseudos');
      }
    }
  }
}

